#!/usr/bin/env python3
"""Check local experiment artifacts (models, predictions, summaries). Outputs Markdown."""
from pathlib import Path

PROJ = Path("/data/zft/deepmd-al-hpc")
EXP = PROJ / "experiments"
OUT = PROJ / "experiments" / "local_artifact_inventory.md"

# Patterns to check per experiment directory
CHECK_FILES = [
    "frozen_model.pb",
    "train.log",
    "test.log",
    "lcurve.out",
    "checkpoint",
    "committee_predictions.npz",
    "selected_topk.json",
    "metrics_summary.csv",
    "metrics_summary.md",
    "prediction_summary.csv",
    "prediction_summary.md",
]


def file_status(p: Path) -> str:
    if not p.exists():
        return "—"
    size = p.stat().st_size
    if size > 1_000_000:
        return f"{size/1e6:.1f}MB"
    if size > 1000:
        return f"{size/1000:.1f}KB"
    return f"{size}B"


def main() -> None:
    lines = [
        "# Local Experiment Artifact Inventory",
        "",
        "Generated: 2026-05-25 | Host: shared-v100",
        "",
        "**Note**: Large files (`.pb`, `.npz`, train.log, checkpoint) are NOT committed to Git.",
        "This inventory is for local reference only.",
        "",
    ]

    # Scan experiment directories
    exp_dirs = sorted([d for d in EXP.iterdir() if d.is_dir() and d.name.startswith("exp_")])
    baseline_dirs = sorted([d for d in (EXP / "baselines").iterdir() if d.is_dir()]) if (EXP / "baselines").exists() else []

    all_dirs = [(f"experiments/{d.name}", d) for d in exp_dirs] + \
               [(f"experiments/baselines/{d.name}", d) for d in baseline_dirs]

    lines.append("| Directory | frozen_model.pb | train.log | selected_topk.json | prediction_summary.csv | metrics_summary.csv |")
    lines.append("|---|---|---|---|---|---|")

    for label, d in all_dirs:
        # Check at top level and model_*/ subdirs
        pb_count = len(list(d.glob("**/frozen_model.pb")))
        log_count = len(list(d.glob("**/train.log")))
        topk = file_status(d / "selected_topk.json")
        pred = file_status(d / "prediction_summary.csv")
        metrics = file_status(d / "metrics_summary.csv")

        pb_str = f"{pb_count}" if pb_count > 0 else "—"
        log_str = f"{log_count}" if log_count > 0 else "—"

        lines.append(f"| {label} | {pb_str} | {log_str} | {topk} | {pred} | {metrics} |")

    lines.extend([
        "",
        "## Baseline summary CSVs",
        "",
    ])
    for f in sorted((EXP / "baselines").glob("*.csv")):
        lines.append(f"- `experiments/baselines/{f.name}` ({f.stat().st_size}B)")

    lines.extend([
        "",
        "## Notes",
        "",
        "- `frozen_model.pb`, `train.log`, `checkpoint`, and `committee_predictions.npz` are NOT committed to Git.",
        "- `selected_topk.json`, `*_summary.csv`, and `*_summary.md` ARE committed (lightweight results).",
        "- Generated by `scripts/analysis/check_local_experiment_artifacts.py`.",
    ])

    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text("\n".join(lines))
    print(f"Wrote: {OUT}")


if __name__ == "__main__":
    main()
